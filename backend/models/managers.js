const mongoose = require("mongoose");

const messManagerSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const MessManager = mongoose.model("spit-MessManager", messManagerSchema);
module.exports = MessManager;
